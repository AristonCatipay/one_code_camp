// Concatenation is the process of joining strings together.
// In the first code snippet, concatenation is achieved using the + operator to combine strings and variables.
console.log("Hello, my name " + name + ". I am " + age + " years old.")

// Template literals (String interpolation):
// Template literals are string literals allowing embedded expressions.
// In the second code snippet, template literals are utilized, which are enclosed by backticks (`).
// Within template literals, you can embed expressions directly using ${} syntax.
console.log(`Hello, my name ${name}. I am ${age} years old.`)